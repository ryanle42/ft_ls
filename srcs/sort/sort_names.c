#include <ftls.h>

void	sort_names(int i, int max, char **argv)
{
	while (i < max && argv[i][0] == '-')
		i++;
	while (i + 1 < max)
	{
		if (!(is_sorted(argv[i], argv[i + 1])))
			ft_strswap(argv[i], argv[i + 1]);
		i++;
	}
}