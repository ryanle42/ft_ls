#include <ftls.h>

int		is_sorted(char *str1, char *str2)
{
	int i;

	i = 0;
	while (str1[i] && str2[i])
	{
		if (str1[i] != str2[i])
			return (str1[i] < str2[i] ? 1 : 0);
		i++;
	}
	if (!str1[i] && str2[i])
		return (1);
	return (0);
}